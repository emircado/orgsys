    <td width="519" align="left"><h2 id="toc0">Guidelines on Renewal of Recognition of All UP COE-Based Organizations</h2>
      <em>by Prof. Ji Reyes</em><br />
      <em>Associate Dean for Student Affairs</em><br />
      <em>UP College of Engineering</em><br />
      <br />
      <h2 id="toc1"><img src="images/next.png" width="24" height="24" /> What needs to be submitted?</h2>
      <ol>
        <li>Report on previous AY's activities</li>
        <li>Approved plan of activities for the incoming school year</li>
        <li>Signed consent of the faculty adviser</li>
        <li>List of org officers with complete and true (not bogus) contact details and Form 5's</li>
        <li>List of members with Form 5's (to be updated at the beginning of the 2nd semester)</li>
        <li>Location plan of tambayan</li>
      </ol>
      <h2 id="toc2"><img src="images/next.png" alt="" width="24" height="24" /> Report on previous AY's activities</h2>
      <ul>
        <li>Attach to this report all documentary evidence (photographs, photocopies of certificates, receipts, etc.) of all accomplished activities. These evidence will be cited in the &quot;Remarks&quot; portion of each accomplished activity.</li>
        <li>For unaccomplished activities, write under the &quot;Remarks&quot; heading the reason for non-accomplishment.</li>
        <li>Signed by current org head and noted by faculty adviser</li>
        <li>Format is that of previous year's proposed plan</li>
      </ul>
      <h2 id="toc3"><img src="images/next.png" alt="" width="24" height="24" /> Approved plan of activities for the incoming schoolyear</h2>
      <ul>
        <li>Concerns all activities with parties outside of the organization (GA's are not included)</li>
        <li>Must include Eng'g Week activities</li>
        <li>Presented first to unit heads for their comments, amendments, and subsequent approval</li>
        <li>Signed by org head, noted by faculty adviser and approved by the unit head</li>
        <li>See format <a href="https://www.dropbox.com/s/dpsqyryywls2pas/org%20recognition%20year%20plan.pdf" rel="nofollow" target="_blank">here</a></li>
      </ul>
      <h2 id="toc4"><img src="images/next.png" alt="" width="24" height="24" /> Consent Form of Faculty Adviser</h2>
      <ul>
        <li>Listed duties and responsibilities of the adviser</li>
        <li>Signed by the adviser and noted by the unit head</li>
        <li>If proposed adviser refuses to sign form, that person forfeits the privilege of being the faculty adviser</li>
        <li>See form <a href="https://www.dropbox.com/s/7n9qy4hhkx4779p/CONSENT%20FORM%20FOR%20FACULTY%20ADVISORS%20OF%20UP.pdf" rel="nofollow" target="_blank">here</a></li>
      </ul>
      <h2 id="toc5"><img src="images/next.png" alt="" width="24" height="24" /> List of org officers</h2>
      <ul>
        <li>Complete list</li>
        <li>Includes contact details (email addresses and mobile phone numbers)</li>
        <li>Must be updated in the 2nd sem</li>
        <li>Will be kept confidential</li>
      </ul>
      <h2 id="toc6"><img src="images/next.png" alt="" width="24" height="24" /> List of members</h2>
      <ul>
        <li>Summarized list (names in alphabetical order) of ALL members</li>
        <li>Include courses (for engg-wide orgs)</li>
        <li>Form 5 attached</li>
        <li>must be updated in the 2nd semester</li>
        <li>Will be kept confidential by the ADSA</li>
        <li>Do not black-out schedules of members who belong to frats, sororities, and other groups</li>
      </ul>
      <h2 id="toc7"><img src="images/next.png" alt="" width="24" height="24" /> Location plan of tambayan</h2>
      <ul>
        <li>Indicate actual location</li>
        <li>Cite landmarks within tambayan vicinity</li>
        <li>Area measurements NOT included here (separate item, for Associate Dean of Administration requirements)</li>
      </ul></td>
